// COMEDIES
$("#Comedy").click(function(event) {
  var buttonPushed = event.target;
  var genre = Comedy //buttonPushed.id;
  var container = $("#container")
  var button = document.querySelector('#Comedy')
  container.empty();

  for (var i = 0; i < movies.length; i++) {
    if (movies[i].Comedy) //[genre])
    {

      var movieImg = $("<img>");
      if (movies[i].image) {
        movieImg.attr('src', movies[i].image);
      } else {
        movieImg.attr('src', "http://lorempixel.com/32/32");
      }
      container.append(movieImg);

      var title = $('<h3>');
      title.text(movies[i].title);
      container.append(title);

      var format = $("<div>");
      format.text(movies[i].format);
      format.addClass("format");
      container.append(format);

      var rating = $("<p>");
      rating.text(movies[i].rating);
      container.append(rating);

      var notes = $("<p>");
      notes.text(movies[i].notes);
      container.append(notes);

      var line = ("<hr>");
      container.append(line);
    }
  }

  if (button.classList.contains('active')) {
    button.classList.remove('active')
    container.empty();
    const h3 = document.createElement('h3')
    h3.innerText = "What movie will you watch? Select a genre!"
    container.append(h3)
  } else {
    button.classList.add('active')
  }
})


